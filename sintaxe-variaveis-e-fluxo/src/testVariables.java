public class testVariables {
    public static void main(String[] args) {
        // declarando variavel do tipo inteiro
        int age; // do tipo inteiro
        age = 28; // atribuindo valor a variavel
        System.out.println("A idade e " + age + " anos"); // imprimindo o valor da variavel

        age = 28 + 5;
        System.out.println("A idade e " + age + " anos"); // imprimindo o valor da variavel

        age = (28 * 2) + 4;
        System.out.println("A idade e " + age + " anos"); // imprimindo o valor da variavel
    }
}
