public class TestaWhile {
    public static void main(String[] args) {
        // vamos fazer uma contagem de 0 a 10 usando while
        int contador = 0;
        while(contador <= 10) {
            System.out.println("Número " + contador); // vai imprimir na tela a partir do número 0
            contador += 1;
        }
    }
}
